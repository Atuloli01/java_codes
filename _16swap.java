public class _16swap{
    public static void main(String []args){
        int num1=24;
        int num2=66;
        int num3;
        num3=num1;
        num1=num2;
        num2=num3;
        System.out.println("First number after swapping is: "+num1);
        System.out.println("Second number after swapping is: "+num2);
    }
}